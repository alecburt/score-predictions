
def check_name(name):
    if name == "alec":
        team_name = name.replace("alec", "Alec Burt") 
        return team_name
    
    if name == "ArmyAmarillo":
        team_name = name.replace("ArmyAmarillo", "Alec Burt") 
        return team_name
        
    if name == "ari":
        team_name = name.replace("ari", "Ari Ordeix") 
        return team_name

    if name == "simon":
        team_name = name.replace("simon", "Simon Furness")
        return team_name
    
    if name == "dad":
        team_name = name.replace("dad", "Norrie Burt")
        return team_name
    
    else:
        team_name = name
        return team_name

